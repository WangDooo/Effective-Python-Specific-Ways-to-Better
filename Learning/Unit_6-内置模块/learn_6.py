# 2. 有序字典 OrderredDict
a = {}
a['foo'] = 1
a['bar'] = 2

while True:
	z = random.randint(1, 20)
	b = {}
	for i in range(z):
		b[i] = i
	b['foo'] = 1
	b['bar'] = 2
	for i in range(z):
		del b[i]
	if str(b) != str(a):
		break

print(a)
print(b)
print(a==b)

